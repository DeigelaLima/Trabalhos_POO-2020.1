package JunkFood_machine;

public class Espiral {

	public String nome;
	public int qtd;
	public float preco;
	
	public Espiral() {
		this.nome = nome;
		this.qtd = qtd;
		this.preco = preco;
	}

	@Override
	public String toString() {
		return "[" + this.nome + ": " + this.qtd + " U : " + this.preco + " R$" + "]";
	}
	
	
	
	
}
